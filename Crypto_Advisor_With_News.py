        "def generate_response(query, max_tokens=300, temperature=0.7, top_p=0.9, top_k=4):\n",
        "    # Step 1: Over-fetch chunks\n",
        "    all_docs = vectordb.similarity_search(query, k=top_k * 5)\n",
        "\n",
        "    # Step 2: Deduplicate based on URL while keeping only the best chunk per article\n",
        "    seen_urls = {}\n",
        "    for doc in all_docs:\n",
        "        url = doc.metadata.get(\"url\")\n",
        "        if not url:\n",
        "            continue\n",
        "\n",
        "        # Prefer the first or latest-scoring chunk\n",
        "        if url not in seen_urls:\n",
        "            seen_urls[url] = doc\n",
        "\n",
        "    # Step 3: Score by recency\n",
        "    def recency_boost(doc):\n",
        "        try:\n",
        "            pub_date = datetime.strptime(doc.metadata.get(\"publish_time\", \"\"), \"%Y-%m-%d\")\n",
        "            days_old = (datetime.now() - pub_date).days\n",
        "            return 1 / (1 + days_old)\n",
        "        except:\n",
        "            return 0\n",
        "\n",
        "    unique_docs = list(seen_urls.values())\n",
        "    sorted_docs = sorted(unique_docs, key=recency_boost, reverse=True)[:top_k]\n",
        "\n",
        "    # Step 4: Compose prompt\n",
